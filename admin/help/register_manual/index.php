    <div class="box_inner">
      <h1>一度登録した情報を修正したい時は</h1>
      <div>
        <p>物販レジで登録した情報は、登録した当日に限り修正可能です。</p>
        <h3>修正できる箇所</h3>
        <ul>
          <li>店舗名</li>
          <li>物販担当</li>
          <li>レジ担当</li>
          <li>商品個数</li>
          <li>支払方法</li>
        </ul>
        <h3>修正方法</h3>
        <ul class="branch_nav brown">
          <li><a href="#branch7_A">顧客を間違えて登録してしまった。</a></li>
          <li><a href="#branch7_B">購入がキャンセルになった。</a></li>
          <li><a href="#branch7_C">購入商品の個数を変更したい。</a></li>
          <li><a href="#branch7_D">購入商品の種類を変更したい。</a></li>
        </ul>
        <dl class="branch">
          <dt id="branch7_A">A.顧客を間違えて登録してしまった。</dt>
          <dt id="branch7_B">B.購入がキャンセルになった。</dt>
            <dd>店舗では修正できません。システム対応へ連絡してください。</dd>
          <dt id="branch7_C">C.購入商品の個数を変更したい。</dt>
            <dd>
              <h3>物販レジ清算終了後の画面から修正可能です。</h3>
              <div>
                <p>1.「レジ」＞「物販一覧」を表示してください。</p>
                <p class="img01_4"></p>
                <p>2.「清算ID」か、「顧客氏名」をクリックすると清算済みの物販レジ画面が表示されます。<span class="comment">※クリックできる文字は下線が付いています。</span></p>
                <p class="img02_4"></p>
                <p class="img01_7"></p>
                <p>3.「商品名・個数入力」で、購入する個数を入力します。</p>
                <div class="indent1">
                  <p class="brown">（例）モイスチャーローション2個から1個に変更する。</p>
                  <p class="brown indent1">
                    モイスチャーローションの個数入力欄に「1」を入力してください。
                    <span class="red comment">※商品個数を全て「0」にして修正する事はできません。</span>
                  </p>
                </div>
                <p class="img02_7"></p>
                <p>4.支払方法を選択します。</p>
                <p class="img03_7"></p>
                <p>5.「修正する」を押すと画面が更新されます。</p>
                <p>6.「購入履歴」を確認してください。購入履歴が正しい商品・数字になっていたら登録完了です。</p>
                <p class="img04_7"></p>
              </div>
            </dd>
          <dt id="branch7_D">D.購入商品の種類を変更したい。</dt>
          <dd>
              <h3>物販レジ清算終了後の画面から修正可能です。</h3>
                <p>1.「レジ」＞「物販一覧」を表示してください。</p>
                <p class="img01_4"></p>
                <p>2.「清算ID」か、「顧客氏名」をクリックすると清算済みの物販レジ画面が表示されます。<span class="comment">※クリックできる文字は下線が付いています。</span></p>
                <p class="img02_4"></p>
                <p class="img01_7"></p>
                <p>3.「商品名・個数入力」で、購入する個数を入力します。</p>
                <div class="indent1">
                  <p class="brown">（例）モイスチャーローション1個から「テスト商品」1個に変更する</p>
                  <p class="brown indent1">
                    「テスト商品」の個数入力欄に「1」を入力してください。
                    <span class="red comment">※商品個数を全て「0」にして修正する事はできません。</span>
                  </p>
                </div>
                <p class="img05_7"></p>
                <p>4.支払方法を選択します。</p>
                <p class="img03_7"></p>
                <p>5.「修正する」を押すと画面が更新されます。</p>
                <p>6.「購入履歴」を確認してください。購入履歴が正しい商品・数字になっていたら登録完了です。</p>
                <p class="img06_7"></p>
          </dd>
        </dl>
      </div>
