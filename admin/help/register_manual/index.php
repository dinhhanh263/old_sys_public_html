      <h1>間違えて2回以上物販レジ清算してしまったと思った時は</h1>
      <div>
        <h3>1.はじめに「レジ」＞「レジ一覧」を確認してください。</h3>
          <dl class="branch">
            <dt>A.お客様の「物販」の行が1行だけで、金額も合っている。</dt>
            <dd>販売商品の履歴を確認します。2へ進んでください。</dd>
            <dt>B.お客様の「物販」の行が2行以上ある。</dt>
            <dd>
              2回以上物販レジ清算が行われています。<br>
              削除したい行の「店舗名」「日付」「会員番号」「顧客氏名」「オプション金額」「現金入金」「カード入金」をシステム部まで連絡してください。<br>
              <!-- 削除したい行の「店舗名」「日付」「会員番号」「顧客氏名」「オプション金額」「現金入金」「カード入金」と、<br>正しい行の「店舗名」「日付」「会員番号」「オプション金額」「現金入金」「カード入金」、「実際に販売した商品の種類と個数」をシステム部まで連絡してください。<br> -->2には進まないでください。
            </dd>
          </dl>
        <h3>2.「レジ」＞「物販一覧」を表示してください。</h3>
        <p>間違えて2回以上物販レジ清算をしたお客様のお名前の行を確認してください。</p>
          <dl class="branch">
            <dt>A.お客様の行を全て確認し、販売した商品の種類と個数が合っている。</dt>
            <dd>販売履歴の登録は正常です。全ての物販レジ清算登録は正常に行われています。<br>システム対応は不要です。</dd>
            <dt>B.お客様の行を全て確認し、販売した商品の種類と個数が間違っている。</dt>
            <dd>
              2回以上物販レジ清算が行われた可能性があります。<br>
              「清算ID」「管理No」「会員番号」「顧客氏名」と、「実際に販売した商品の種類と個数」をシステム部まで連絡してください。
            </dd>
          </dl>
